#!/bin/bash

# Счетчик для нумерации файлов
counter=1

# Перебираем все файлы в текущей директории
for file in *; do
    # Пропускаем директории и сам скрипт
    if [[ -f "$file" ]] && [[ "$file" != "${0##*/}" ]]; then
        # Формируем новое имя файла
        new_name="${counter}.jpeg"
        
        # Переименовываем файл
        mv "$file" "$new_name"
        
        echo "Переименован: $file -> $new_name"
        
        # Увеличиваем счетчик
        ((counter++))
    fi
done

echo "Готово! Переименовано $((counter-1)) файлов."